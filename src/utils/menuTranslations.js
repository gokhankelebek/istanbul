/**
 * Get translated menu item name
 * @param {string} name - Original menu item name
 * @param {string} language - Target language code
 * @returns {string} - Translated name or original if not found
 */
export function getMenuItemTranslation(name, language = 'en-us') {
  if (language === 'en-us') return name;
  
  const translations = menuTranslations[language];
  if (!translations || !translations.items) return name;
  
  // Check for exact match first
  if (translations.items[name]) {
    return translations.items[name];
  }
  
  // Check for partial matches (for compound names)
  for (const [key, value] of Object.entries(translations.items)) {
    if (name.includes(key)) {
      return name.replace(key, value);
    }
  }
  
  return name;
}

/**
 * Get translated category name
 * @param {string} category - Original category slug
 * @param {string} language - Target language code
 * @returns {string} - Translated category or original if not found
 */
export function getCategoryTranslation(category, language = 'en-us') {
  if (language === 'en-us') {
    // Convert slug to readable English
    return category
      .split('-')
      .map(word => word.charAt(0).toUpperCase() + word.slice(1))
      .join(' ');
  }
  
  const translations = menuTranslations[language];
  if (!translations || !translations.categories) {
    return category;
  }
  
  return translations.categories[category] || category;
}

/**
 * Get translated description
 * @param {string} description - Original description
 * @param {string} language - Target language code
 * @param {string} itemType - Type of item (doner, falafel, etc.)
 * @returns {string} - Translated description or original if not found
 */
export function getDescriptionTranslation(description, language = 'en-us', itemType = '') {
  if (language === 'en-us') return description;
  
  const translations = menuTranslations[language];
  if (!translations || !translations.descriptions) return description;
  
  // For specific item types, use predefined translations
  if (itemType && translations.descriptions[itemType]) {
    return translations.descriptions[itemType];
  }
  
  // For general descriptions, check if it contains keywords
  const lowerDesc = description.toLowerCase();
  
  if (lowerDesc.includes('doner') || lowerDesc.includes('döner') || lowerDesc.includes('shawarma')) {
    return translations.descriptions.doner || description;
  }
  
  if (lowerDesc.includes('falafel')) {
    return translations.descriptions.falafel || description;
  }
  
  if (lowerDesc.includes('baklava')) {
    return translations.descriptions.baklava || description;
  }
  
  if (lowerDesc.includes('hummus')) {
    return translations.descriptions.hummus || description;
  }
  
  return description;
}